SOURCE_HEADERS = {
    "ios_appstore": "=== Отзывы iOS / App Store ===\n",
    "android_play": "=== Отзывы Android / Google Play ===\n",
    "rustore": "=== Отзывы RuStore ===\n",
    "wave_summer_2025": "=== Волна отзывов: май — август 2025 ===\n",
    "wave_autumn_2025": "=== Волна отзывов: сентябрь — ноябрь 2025 ===\n",
}


def format_review(author, platform, rating, date, version, text) -> str:
    return (
        f"---\n"
        f"Автор: {author}\n"
        f"Платформа: {platform}\n"
        f"Оценка: {rating}\n"
        f"Дата: {date}\n"
        f"Версия: {version}\n"
        f"Текст: {text}\n"
        f"---"
    )


def bucket_for_source(name: str, platform: str, date: str) -> str:
    if name == "ios_appstore":
        return platform == "ios"
    if name == "android_play":
        return platform == "android"
    if name == "rustore":
        return platform == "rustore"
    if name == "wave_summer_2025":
        return date < "2025-09-01"
    if name == "wave_autumn_2025":
        return date >= "2025-09-01"
    return False


def main() -> None:
    out_dir = Path("input")
    out_dir.mkdir(parents=True, exist_ok=True)

    all_blocks = [
        format_review(*row)
        for row in REVIEWS
    ]
    (out_dir / "reviews.txt").write_text(
        "=== Отзывы на приложение «Сфера Банк» (полный датасет) ===\n\n"
        + "\n".join(all_blocks)
        + "\n",
        encoding="utf-8",
    )

    for source_name, header in SOURCE_HEADERS.items():
        blocks = [
            format_review(*row)
            for row in REVIEWS
            if bucket_for_source(source_name, row[1], row[3])
        ]
        (out_dir / f"{source_name}.txt").write_text(
            header + "\n" + "\n".join(blocks) + "\n",
            encoding="utf-8",
        )
        print(f"  {source_name}.txt — {len(blocks)} отзывов")

    print(f"\nИтого: {len(REVIEWS)} отзывов → input/reviews.txt + 5 источников")


if __name__ == "__main__":
    main()
